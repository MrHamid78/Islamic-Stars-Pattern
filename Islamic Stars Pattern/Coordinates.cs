using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Islamic_Stars_Pattern
{
    public class Coordinates
    {
        private double x;
        private double y;
        public Coordinates()
        {

        }

        public double X
        {
            get { return x; }
            set { x = value; }
        }

        public double Y
        {
            get { return y; }
            set { y = value; }
        }
    }
}
